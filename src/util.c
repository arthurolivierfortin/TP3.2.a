void functionsExercise1Q1(int T[], int S[], int taille_T) {
    int i, j;
    
    for(i=0;i<taille_T;i++){
        if(T[i]!=0){
            S[j]=T[i];
            j+=1;
        }
    }

    for(j=j;j<taille_T;j++){
        S[j]=0;
    }

    return S;
}


